"""HDF5-backed trajectory dataset.

Stores and loads (state, action) trajectory pairs generated by the double pendulum
simulator. States are (T+1, 4) arrays [q1, q2, dq1, dq2]; actions are (T, 2) torques.
"""

from __future__ import annotations

from pathlib import Path
from typing import Union

import h5py
import numpy as np


class TrajectoryDataset:
    """Trajectory dataset backed by an HDF5 file.

    On construction, the full dataset is loaded into RAM for fast access.

    Attributes:
        states: (N, T+1, 4) state trajectories.
        actions: (N, T, 2) action sequences.
        n_steps: number of control steps per trajectory (T).
    """

    def __init__(self, path: Union[str, Path]) -> None:
        self.path = Path(path)
        self._f = h5py.File(self.path, "r")
        self.states: np.ndarray = self._f["states"][:]   # (N, T+1, 4)
        self.actions: np.ndarray = self._f["actions"][:] # (N, T, 2)

    @property
    def n_steps(self) -> int:
        """Number of control steps per trajectory."""
        return self.actions.shape[1]

    def __len__(self) -> int:
        return len(self.states)

    def __getitem__(self, i: int) -> tuple[np.ndarray, np.ndarray]:
        """Return (states, actions) for trajectory i."""
        return self.states[i], self.actions[i]

    def get_all_states(self) -> np.ndarray:
        """Return all state trajectories as (N, T+1, 4)."""
        return self.states

    def get_all_actions(self) -> np.ndarray:
        """Return all action sequences as (N, T, 2)."""
        return self.actions

    def compute_coverage(self, n_bins: int = 10) -> dict:
        """Compute state-space coverage metrics.

        Bins the (q1, q2) and (dq1, dq2) distributions and reports the
        fraction of bins that contain at least one sample.

        Args:
            n_bins: Number of histogram bins per dimension.

        Returns:
            dict with:
                q_coverage (float): fraction of (q1, q2) bins occupied.
                v_coverage (float): fraction of (dq1, dq2) bins occupied.
                q_hist (n_bins, n_bins): 2-D position histogram.
                v_hist (n_bins, n_bins): 2-D velocity histogram.
                q_edges (n_bins+1,): bin edges for both q axes.
                v_edges (n_bins+1,): bin edges for both v axes.
        """
        q1 = self.states[:, :, 0].ravel()
        q2 = self.states[:, :, 1].ravel()
        v1 = self.states[:, :, 2].ravel()
        v2 = self.states[:, :, 3].ravel()

        q_range = [-np.pi, np.pi]
        q_hist, q_xedges, q_yedges = np.histogram2d(
            q1, q2, bins=n_bins, range=[q_range, q_range]
        )
        q_edges = q_xedges  # symmetric, same for both axes

        v_lim = max(float(np.abs(v1).max()), float(np.abs(v2).max()), 1.0)
        v_range = [-v_lim, v_lim]
        v_hist, v_xedges, _ = np.histogram2d(
            v1, v2, bins=n_bins, range=[v_range, v_range]
        )
        v_edges = v_xedges

        return {
            "q_coverage": float(np.sum(q_hist > 0) / n_bins ** 2),
            "v_coverage": float(np.sum(v_hist > 0) / n_bins ** 2),
            "q_hist": q_hist,
            "v_hist": v_hist,
            "q_edges": q_edges,
            "v_edges": v_edges,
        }

    def close(self) -> None:
        """Close the underlying HDF5 file."""
        self._f.close()
